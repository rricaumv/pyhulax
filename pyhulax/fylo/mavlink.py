        MAVLink_message.__init__(self, MAVLINK_MSG_ID_BAD_DATA, 'BAD_DATA')
        self._fieldnames = ['data', 'reason']
        self.data = data
        self.reason = reason
        self._msgbuf = data
        self._instance_field = None

